namespace wi19b090_02._12._2020
{
    internal class Calc
    {
        public Calc()
        {
        }

        internal int add(int p0, int p1)
        {
            return p0 + p1;
        }

        internal int substract(int p0, int p1)
        {
            return p0 - p1;
        }

        internal int multiply(int p0, int p1)
        {
            return p0 * p1;
        }

        internal int divide(int p0, int p1)
        {
            return p0 / p1;
        }
    }
}